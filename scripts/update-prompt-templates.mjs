import { mkdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'

const SOURCE_URL = 'https://raw.githubusercontent.com/YouMind-OpenLab/awesome-gpt-image-2/main/README_zh.md'
const OUTPUT_PATH = path.resolve('src/lib/promptTemplates.generated.ts')

function slugify(value) {
  return value
    .normalize('NFKD')
    .replace(/[^\p{Letter}\p{Number}]+/gu, '-')
    .replace(/^-+|-+$/g, '')
    .toLowerCase()
    .slice(0, 80) || 'prompt'
}

function extractFirst(pattern, text) {
  const match = text.match(pattern)
  return match ? match[1].trim() : ''
}

function detectCategory(title, featured) {
  if (featured) return '精选'
  const separatorIndex = title.indexOf(' - ')
  if (separatorIndex > 0) return title.slice(0, separatorIndex).trim()
  const knownCategories = [
    '个人资料 / 头像',
    '社交媒体帖子',
    '信息图 / 教育视觉图',
    'YouTube 缩略图',
    '漫画 / 故事板',
    '产品营销',
    '电商主图',
    '游戏素材',
    '海报 / 传单',
    'App / 网页设计',
    '摄影',
    '电影 / 电影剧照',
    '动漫 / 漫画',
    '插画',
    '草图 / 线稿',
    '3D 渲染',
    'Q 版 / Q 萌风',
    '等距',
    '像素艺术',
    '油画',
    '水彩画',
    '水墨 / 中国风',
    '复古 / 怀旧',
    '赛博朋克 / 科幻',
    '极简主义',
    '人像 / 自拍',
    '产品',
    '食品 / 饮料',
    '建筑 / 室内设计',
    '风景 / 自然',
    '文本 / 排版',
  ]
  return knownCategories.find((category) => title.includes(category)) ?? '社区提示词'
}

function parseReadme(markdown) {
  const headings = [...markdown.matchAll(/^### No\.\s*(\d+):\s*(.+)$/gm)]
  return headings.map((heading, index) => {
    const start = heading.index
    const end = headings[index + 1]?.index ?? markdown.length
    const block = markdown.slice(start, end)
    const title = heading[2].trim()
    const inFeaturedSection = markdown.lastIndexOf('## 🔥 精选提示词', start) > markdown.lastIndexOf('## 📋 所有提示词', start)
    const prompt = extractFirst(/#### 📝 提示词\s*\n\s*```\s*\n([\s\S]*?)\n```/, block)
    const author = extractFirst(/- \*\*作者:\*\* \[([^\]]+)\]\([^)]+\)/, block) || 'YouMind OpenLab 社区'
    const summary = extractFirst(/#### 📖 描述\s*\n\s*([\s\S]*?)\n\s*#### 📝 提示词/, block)
      .replace(/\n+/g, ' ')
      .trim()
    const languageTags = [...block.matchAll(/Language-([A-Z]+)/g)].map((match) => match[1])
    const featured = inFeaturedSection || block.includes('Featured')
    const raycast = block.includes('Raycast_Friendly')
    const category = detectCategory(title, featured)
    const tags = [
      category,
      featured ? '精选' : '',
      raycast ? '可变参数' : '',
      ...languageTags.map((language) => `语言-${language}`),
    ].filter(Boolean)

    return {
      id: `awesome-gpt-image-2-${index + 1}-${slugify(title)}`,
      title,
      prompt,
      summary,
      category,
      tags: [...new Set(tags)],
      imageUrl: '',
      author,
    }
  }).filter((template) => template.prompt)
}

async function readSource() {
  const localPath = process.argv[2]
  if (localPath) return readFile(localPath, 'utf8')
  const response = await fetch(SOURCE_URL, {
    headers: {
      'User-Agent': 'ov-image-studio-prompt-template-updater',
    },
  })
  if (!response.ok) {
    throw new Error(`Failed to fetch ${SOURCE_URL}: ${response.status}`)
  }
  return response.text()
}

const markdown = await readSource()
const templates = parseReadme(markdown)
if (templates.length < 100) {
  throw new Error(`Expected at least 100 prompt templates, got ${templates.length}`)
}

const content = `// Generated by scripts/update-prompt-templates.mjs from ${SOURCE_URL}
// Source project: YouMind-OpenLab/awesome-gpt-image-2, licensed under CC BY 4.0.
// Runtime image URLs are intentionally omitted so the app does not load external template assets.

export const GENERATED_PROMPT_TEMPLATES = ${JSON.stringify(templates, null, 2)} as const
`

await mkdir(path.dirname(OUTPUT_PATH), { recursive: true })
await writeFile(OUTPUT_PATH, content, 'utf8')
console.log(`Generated ${templates.length} prompt templates at ${OUTPUT_PATH}`)
